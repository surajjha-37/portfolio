// comparison operator
console.log(5 === 5);
console.log(5 === "5");
console.log(5 != 5);
console.log(5 != 6);

// Assignment operator
let a = 5;
console.log((a += 5));
console.log((a *= 5));
console.log((a /= 5));

// logical operator
console.log(!(5 > 3 && 3 > 5));
console.log(5 > 3 && 3 > 5);
console.log(5 > 3 || 3 > 5);

// Arithmatic Operator

console.log(2 + 2);
console.log(2 - 2);
console.log(2 * 2);
console.log(2 / 2);
console.log(2 ** 2);

// Ternary Operator
let b = 5;
let c = "suraj";
console.log(typeof b);
console.log(typeof c);
